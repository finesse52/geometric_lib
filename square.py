def area(a):
    '''
    Возвращает площадь квадрата (a, a) в виде числа:
        Параметры:
                a (int) : Сторона квадрата
        Возвращаемое значение:
                a * a
    '''
    return a * a


def perimeter(a):
    '''
    Возвращает периметр квадрата (a, a) в виде числа:
        Параметры:
                a (int) : Сторона квадрата
        Возвращаемое значение:
                4 * a
    '''
    return 4 * a
